# Creamos una lista llamada 'numeros' con cinco elementos del 1 al 5
numeros = [1, 2, 3, 4, 5]

# Usamos el método .clear() para eliminar todos los elementos de la lista
# Esto deja la lista vacía, pero la variable 'numeros' sigue existiendo
numeros.clear()

# Imprimimos el contenido actual de la lista
# Como la lista fue vaciada, mostrará: []
print(numeros)
